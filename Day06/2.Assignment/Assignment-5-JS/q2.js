let a = 5;
let b = 10;

let sum = a + b;

console.log(`The sum of ${a} and ${b} is: ${sum}`);

let difference = b - a;

console.log(`The difference when ${a} is subtracted from ${b} is: ${difference}`);  

let product = a * b;

console.log(`The product of ${a} and ${b} is: ${product}`);

let quotient = b / a;

console.log(`The quotient when ${b} is divided by ${a} is: ${quotient}`);

let remainder = b % a;

console.log(`The remainder when ${b} is divided by ${a} is: ${remainder}`);
